
//Conditional Statements 
//1.if -else
let m=18;

if(m > 17)
    {
        console.log("m is greater");
    }
    else{
        console.log("m is smaller");
    }


//2.Switch-Case
let choice=3;

switch(choice){
    case 1:
           console.log("case 1");
           break;
    case 2 :   
             console.log("case 2");
             break;
    case 3  :  
             console.log("case 3");
             break;    
             
    default :  
               console.log("case default");
               break;         
}
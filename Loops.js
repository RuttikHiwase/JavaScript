//Looping Statements in Javascript

//1.for Loop - use when you know how many times to rotate loop
for(let i=1; i<=10;i++)
    {
        console.log(i);
    }


//2.while loop - use when you don't know how many times do loop rotate.
let j=1;
while(j <= 10){
    console.log(j);
    j++;
}    

//3.do- while loop- use when you have execute the block atleast once.
let s=4;
do{
    console.log(s);
    s++;

}while(s <=10);